#include "push_swap.h"

void	free_the_split(char **split)
{
	int	i;

	i = 0;
	while (split[i] != NULL)
		i++;
	i -= 1;
	while (i >= 0)
	{
		free(split[i]);
		i--;
	}
	free(split);
}
